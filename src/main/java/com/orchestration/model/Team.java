package com.orchestration.model;

public class Team
{
    private int id;

    private String teamName;

    private String teamState;

    public Team()
    {
    }

    public int getId() {
        return id;
    }

    public String getTeamName() {
        return teamName;
    }

    public String getTeamState() {
        return teamState;
    }
}
