package com.orchestration.model;

public class Player
{
    private int id;

    private String firstName;

    private String lastName;

    public Player()
    {
    }

    public int getId()
    {
        return id;
    }

    public String getFirstName()
    {
        return firstName;
    }

    public String getLastName()
    {
        return lastName;
    }
}
